    [Test]
    public void RunServer()
    {
        var settings = new ServerSettings().AddUniverse(new ArtNetUniverse(1)).EnableDebug().AddIPAddress(new Regex("192.*"));
        var server = new Server(settings);
        server.Run();
        while (true)
        {
            if (Server.Logs.TryDequeue(out string? log))
                TestContext.Out.WriteLine(log);
                // Console.WriteLine(log);
            
            if (!server.Data.TryDequeue(out DMXUniverseData? data))
                continue;
            
            TestContext.Out.WriteLine($"data received: {data.Data.Count}");
            // Console.WriteLine($"data received: {data.Data.Count}");
            // data.Data;
        }
        Assert.Pass();
    }
    
